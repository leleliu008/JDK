public class NumberTest {

	public static void main(String[] args) {
		System.out.println(10 - 9 == 1);
		System.out.println(1.0f - 0.9f == 0.1f);
		System.out.println(1.0 - 0.9 == 0.1);
		System.out.println(10 * (1.0 - 0.9) == 1);
	}
}
